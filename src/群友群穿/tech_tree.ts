/** 获取所有科技节点名列表 */
export function 所有节点名(): string[] {
  return Object.keys(科技树);
}

/** 获取某节点的前置科技列表 */
export function 获取前置科技(节点名: string): string[] {
  return 科技树[节点名]?.前置科技 ?? [];
}

/** 检查所有前置科技是否已完成 */
export function 前置科技是否全部完成(节点名: string, 运行时状态: Record<string, { 已研发天数: number }>): boolean {
  const 前置列表 = 获取前置科技(节点名);
  if (前置列表.length === 0) return true;
  return 前置列表.every(前置名 => {
    const 运行时 = 运行时状态[前置名];
    if (!运行时) return false;
    return 运行时.已研发天数 >= (科技树[前置名]?.研发总耗时_天 ?? Infinity);
  });
}

/** 按分支分组获取节点名列表 */
export function 按分支分组(): Record<string, string[]> {
  const 分组: Record<string, string[]> = {};
  for (const [节点名, 定义] of Object.entries(科技树)) {
    if (!分组[定义.所属分支]) 分组[定义.所属分支] = [];
    分组[定义.所属分支].push(节点名);
  }
  return 分组;
}

/** 计算科技自主度 */
export function 计算科技自主度(运行时状态: Record<string, { 已研发天数: number }>): number {
  let 总完成数 = 0;
  let 非依赖数 = 0;
  for (const [节点名, 定义] of Object.entries(科技树)) {
    const 运行时 = 运行时状态[节点名];
    if (!运行时 || 运行时.已研发天数 < 定义.研发总耗时_天) continue;
    总完成数++;
    if (!定义.备注.includes('依赖基地车')) 非依赖数++;
  }
  if (总完成数 === 0) return 0;
  return 非依赖数 / 总完成数;
}
