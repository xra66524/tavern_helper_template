// Vue 响应式 Hook（供组件使用）
// ════════════════════════════════════════════════════════════

export function useTechTreeData(主要研究槽: Ref<研究槽[]>, 次要研究槽: Ref<研究槽[]>, 已解锁能力清单: Ref<string[]>) {
  /** 已完成节点名集合 */
  const 已完成节点 = computed(() => abilitiesToCompletedNodes(已解锁能力清单.value));

  /** 当前占用了研究槽的节点名集合 */
  const 已占用节点 = computed(() => 获取研究槽节点名(主要研究槽.value, 次要研究槽.value));

  /** 所有节点的全貌列表 */
  const 节点全貌列表 = computed<节点全貌[]>(() =>
    获取所有节点全貌(已完成节点.value, 主要研究槽.value, 次要研究槽.value),
  );

  /** 可添加到研究槽的候选节点列表 */
  const 候选节点列表 = computed<候选节点[]>(() =>
    获取可添加候选节点(已完成节点.value, 主要研究槽.value, 次要研究槽.value),
  );

  /** 分支进度概览 */
  const 分支进度列表 = computed<分支进度[]>(() => 获取分支进度(已完成节点.value));

  /** 按拓扑层级分组的节点（供 DAG 可视化） */
  const 节点按层级分组 = computed(() => {
    const groups: Record<number, 节点全貌[]> = {};
    for (const node of 节点全貌列表.value) {
      if (!groups[node.拓扑层级]) groups[node.拓扑层级] = [];
      groups[node.拓扑层级].push(node);
    }
    return Object.entries(groups)
      .sort(([a], [b]) => Number(a) - Number(b))
      .map(([_, nodes]) => nodes);
  });

  /** 只显示已启动 + 可启动的节点（性能优化筛选） */
  const 活跃节点列表 = computed(() =>
    节点全貌列表.value.filter(
      n => n.状态 === '可启动' || n.状态 === '主要' || n.状态 === '次要' || n.状态 === '已完成',
    ),
  );

  // ── 研究槽容量相关 ──

  /** 主要研究槽当前上限（初始 1 + 已完成整合型节点数） */
  const 主要槽上限 = computed(() => 获取主要研究槽上限(已完成节点.value));

  /** 次要研究槽当前上限（初始 3 + 已完成系统性基础节点数） */
  const 次要槽上限 = computed(() => 获取次要研究槽上限(已完成节点.value));

  /** 主要研究槽是否还可添加新节点 */
  const 主要槽可添加 = computed(() => 主要研究槽是否可添加(主要研究槽.value, 已完成节点.value));

  /** 次要研究槽是否还可添加新节点 */
  const 次要槽可添加 = computed(() => 次要研究槽是否可添加(次要研究槽.value, 已完成节点.value));

  // ── 科技树终局检测 ──

  /** 科技树是否已完成终极目标（大规模科研计划已解锁） */
  const 科技树已完成 = computed(() => 科技树是否已完成(已完成节点.value));

  return {
    已完成节点,
    已占用节点,
    节点全貌列表,
    候选节点列表,
    分支进度列表,
    节点按层级分组,
    活跃节点列表,
    主要槽上限,
    次要槽上限,
    主要槽可添加,
    次要槽可添加,
    科技树已完成,
  };
}
