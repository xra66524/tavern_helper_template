/** 根据选举类型和阶段推导期望的政治阶段 */
function derive政治阶段(类型: string | undefined, 阶段: string): string | null {
  if (类型 === '2039特别大选') {
    if (阶段.includes('报名')) return '特别大选报名期';
    if (阶段.includes('初选')) return '特别大选初选期';
    if (阶段.includes('指名')) return '特别大选指名期';
  }
  if (类型 === '正常首相指名') {
    if (阶段 === '已完成') return '新内阁执政期';
    return '正常选举期';
  }
  return null;
}

/** 安全追加潜在危机（不超过5条） */
function appendCrisis(
  data: MvuData,
  现有: Array<{ 危机: string; 触发条件: string; 严重程度: string }>,
  新危机: { 危机: string; 触发条件: string; 严重程度: string },
) {
  if (现有.length < 5) {
    _.set(data, '事件队列.潜在危机', [...现有, 新危机]);
  }
}

/** 安全追加近期事件（不超过5条） */
function appendEvent(
  data: MvuData,
  现有: Array<{ 事件: string; 时间: string; 影响: string }>,
  新事件: { 事件: string; 时间: string; 影响: string },
) {
  if (现有.length < 5) {
    _.set(data, '事件队列.近期已发生事件', [...现有, 新事件]);
  }
}
