// ============================================================
// src/2039_mvu/index.ts
// 入口文件：注册Schema + VARIABLE_UPDATE_ENDED 兜底校验
//
// 设计原则：
//   跨模块因果推导规则已写入世界书「变量更新规则.yaml」，
//   AI在生成变量更新时应自觉遵守这些规则。
//   此处仅做兜底校验——当AI遗漏了必要的联动更新时，
//   由脚本自动补全，防止变量状态不一致。
// ============================================================

import { registerMvuSchema } from 'https://testingcf.jsdelivr.net/gh/StageDog/tavern_resource/dist/util/mvu_zod.js';
import { Schema } from './mvu_schema';
import type { MvuData } from './types';

$(() => {
  // ─── 注册 Schema ────────────────────────────────────
  registerMvuSchema(Schema);

  // ─── 兜底校验：VARIABLE_UPDATE_ENDED 后处理 ─────────

  void (async () => {
    await waitGlobalInitialized('Mvu');

    eventOn(Mvu.events.VARIABLE_UPDATE_ENDED, (variables, variables_before) => {
      const data = variables.stat_data as MvuData;
      const before = variables_before.stat_data as MvuData;

      // ═══════════════════════════════════════════════════
      // 兜底1：选举进程 → 政治阶段/选举模式一致性校验
      // 规则详见：变量更新规则.yaml → 世界.政治阶段
      // ═══════════════════════════════════════════════════
      if (!_.isEqual(data.日本执政?.选举进程, before.日本执政?.选举进程)) {
        const 阶段 = data.日本执政?.选举进程?.阶段 ?? '';
        const 类型 = data.日本执政?.选举进程?.类型;

        // 选举模式与选举进程.类型不一致 → 兜底同步
        if (类型 !== before.日本执政?.选举进程?.类型 && data.世界?.选举模式 !== 类型) {
          _.set(data, '世界.选举模式', 类型 ?? '无');
        }

        // 正常指名失败 → 兜底触发特别大选
        if (阶段 === '无人愿意出任首相' && 类型 === '正常首相指名') {
          if (data.日本执政?.选举进程?.类型 !== '2039特别大选') {
            _.set(data, '日本执政.选举进程.类型', '2039特别大选');
            _.set(data, '日本执政.选举进程.阶段', '互联网报名');
            _.set(data, '世界.选举模式', '2039特别大选');
            _.set(data, '世界.政治阶段', '特别大选报名期');
          }
        }

        // 政治阶段与选举进程不一致 → 兜底同步
        const 期望政治阶段 = derive政治阶段(类型, 阶段);
        if (期望政治阶段 && data.世界?.政治阶段 !== 期望政治阶段) {
          _.set(data, '世界.政治阶段', 期望政治阶段);
        }
      }

      // ═══════════════════════════════════════════════════
      // 兜底2：首相状态 → 政治阶段一致性校验
      // 规则详见：变量更新规则.yaml → 世界.政治阶段
      // ═══════════════════════════════════════════════════
      if (data.日本执政?.首相状态 !== before.日本执政?.首相状态) {
        if (data.日本执政?.首相状态 === '空位' && data.世界?.政治阶段 !== '看守停摆期') {
          _.set(data, '世界.政治阶段', '看守停摆期');
        } else if (data.日本执政?.首相状态 === '新任') {
          if (data.世界?.政治阶段 !== '新内阁执政期') {
            _.set(data, '世界.政治阶段', '新内阁执政期');
          }
          if (data.日本执政?.选举进程?.类型 !== '无') {
            _.set(data, '日本执政.选举进程.类型', '无');
            _.set(data, '日本执政.选举进程.阶段', '已完成');
            _.set(data, '世界.选举模式', '无');
          }
        }
      }

      // ═══════════════════════════════════════════════════
      // 兜底3：阁僚 ↔ 角色情况双向同步校验
      // 规则详见：变量更新规则.yaml → 日本执政.本届内阁.主要阁僚
      // ═══════════════════════════════════════════════════
      const 阁僚们 = data.日本执政?.本届内阁?.主要阁僚 ?? {};
      const 角色们 = data.各角色情况 ?? {};

      for (const [阁僚职位, 阁僚] of Object.entries(阁僚们)) {
        const 阁僚名 = 阁僚.姓名;
        const 角色 = 角色们[阁僚名];
        if (!角色) continue;

        // 阁僚安全状况 → 角色安全
        if (阁僚.人身安全状况 === '已死亡' && 角色.人身安全 !== '已死亡') {
          _.set(data, ['各角色情况', 阁僚名, '人身安全'], '已死亡');
        } else if ((阁僚.暗杀威胁等级 === '极高' || 阁僚.暗杀威胁等级 === '高') && 角色.人身安全 !== '需要安保') {
          _.set(data, ['各角色情况', 阁僚名, '人身安全'], '需要安保');
        }

        // 阁僚职位 → 角色公职同步
        if (角色.担任公职 !== 阁僚职位) {
          _.set(data, ['各角色情况', 阁僚名, '担任公职'], 阁僚职位);
        }

        // 阁僚职位 → 政治卷入度
        if (角色.政治卷入度 !== '深度卷入') {
          _.set(data, ['各角色情况', 阁僚名, '政治卷入度'], '深度卷入');
        }
      }

      // 反向：角色死亡/失踪 → 阁僚安全状况
      for (const [角色名, 角色] of Object.entries(角色们)) {
        const 匹配阁僚 = Object.entries(阁僚们).find(([, g]) => g.姓名 === 角色名);
        if (!匹配阁僚) continue;
        const [职位, 阁僚] = 匹配阁僚;
        if (角色.人身安全 === '已死亡' && 阁僚.人身安全状况 !== '已死亡') {
          _.set(data, ['日本执政', '本届内阁', '主要阁僚', 职位, '人身安全状况'], '已死亡');
        } else if (角色.人身安全 === '失踪' && 阁僚.人身安全状况 !== '受到威胁') {
          _.set(data, ['日本执政', '本届内阁', '主要阁僚', 职位, '人身安全状况'], '受到威胁');
        }
      }

      // ═══════════════════════════════════════════════════
      // 兜底3b：阁僚 ↔ 省厅大臣姓名一致性校验
      // 规则详见：变量更新规则.yaml → 日本执政.各省厅.大臣
      // ═══════════════════════════════════════════════════
      const 各省厅 = data.日本执政?.各省厅 ?? {};
      const 省厅名映射: Record<string, string> = {
        外务大臣: '外务省',
        防卫大臣: '防卫省',
        经济产业大臣: '经济产业省',
        农林水产大臣: '农林水产省',
        财务大臣: '财务省',
        国土交通大臣: '国土交通省',
        法务大臣: '法务省',
        文部科学大臣: '文部科学省',
        厚生劳动大臣: '厚生劳动省',
        总务大臣: '总务省',
        环境大臣: '环境省',
        国家公安委员会委员长: '国家公安委员会',
        内阁府特命担当大臣: '内阁府',
      };

      for (const [阁僚职位, 阁僚] of Object.entries(阁僚们)) {
        const 对应省厅 = 省厅名映射[阁僚职位];
        if (!对应省厅 || !各省厅[对应省厅]) continue;

        // 阁僚姓名 → 省厅.大臣 同步
        if (各省厅[对应省厅].大臣 !== 阁僚.姓名) {
          _.set(data, ['日本执政', '各省厅', 对应省厅, '大臣'], 阁僚.姓名);
        }

        // 阁僚死亡 → 省厅运作状态降级
        if (阁僚.人身安全状况 === '已死亡') {
          const 当前状态 = 各省厅[对应省厅].运作状态;
          if (当前状态 === '正常') {
            _.set(data, ['日本执政', '各省厅', 对应省厅, '运作状态'], '半瘫痪');
          }
        }
      }

      // 反向：省厅.大臣 → 阁僚姓名同步
      for (const [省厅名, 省厅] of Object.entries(各省厅)) {
        if (!省厅.大臣) continue;
        // 找到对应的阁僚职位
        const 对应职位 = Object.entries(省厅名映射).find(([, v]) => v === 省厅名)?.[0];
        if (!对应职位 || !阁僚们[对应职位]) continue;
        if (阁僚们[对应职位].姓名 !== 省厅.大臣) {
          _.set(data, ['日本执政', '本届内阁', '主要阁僚', 对应职位, '姓名'], 省厅.大臣);
        }
      }

      // ═══════════════════════════════════════════════════
      // 兜底3c：副大臣 ↔ 角色情况双向同步校验
      // 规则详见：变量更新规则.yaml → 日本执政.各省厅.副大臣
      // ═══════════════════════════════════════════════════
      for (const [省厅名, 省厅] of Object.entries(各省厅)) {
        const 副大臣 = 省厅.副大臣;
        if (!副大臣?.姓名) continue;
        const 角色 = 角色们[副大臣.姓名];
        if (!角色) continue;

        // 副大臣安全 → 角色安全
        if (副大臣.人身安全状况 === '已死亡' && 角色.人身安全 !== '已死亡') {
          _.set(data, ['各角色情况', 副大臣.姓名, '人身安全'], '已死亡');
        } else if ((副大臣.暗杀威胁等级 === '极高' || 副大臣.暗杀威胁等级 === '高') && 角色.人身安全 !== '需要安保') {
          _.set(data, ['各角色情况', 副大臣.姓名, '人身安全'], '需要安保');
        }

        // 副大臣职位 → 角色公职同步
        const 公职名 = `${省厅名}副大臣`;
        if (角色.担任公职 !== 公职名) {
          _.set(data, ['各角色情况', 副大臣.姓名, '担任公职'], 公职名);
        }

        // 副大臣 → 政治卷入度
        if (角色.政治卷入度 !== '深度卷入') {
          _.set(data, ['各角色情况', 副大臣.姓名, '政治卷入度'], '深度卷入');
        }
      }

      // 反向：角色死亡/失踪 → 副大臣安全状况
      for (const [角色名, 角色] of Object.entries(角色们)) {
        const 匹配省厅 = Object.entries(各省厅).find(([, s]) => s.副大臣?.姓名 === 角色名);
        if (!匹配省厅) continue;
        const [省厅名] = 匹配省厅;
        if (角色.人身安全 === '已死亡') {
          _.set(data, ['日本执政', '各省厅', 省厅名, '副大臣', '人身安全状况'], '已死亡');
        } else if (角色.人身安全 === '失踪') {
          _.set(data, ['日本执政', '各省厅', 省厅名, '副大臣', '人身安全状况'], '受到威胁');
        }
      }

      // ═══════════════════════════════════════════════════
      // 兜底3d：事务次官 ↔ 事务次官会议派系一致性校验
      // 规则详见：变量更新规则.yaml → 日本执政.各省厅.事务次官
      // ═══════════════════════════════════════════════════
      const 次官会议派系 = data.日本执政?.事务次官会议?.内部派系 ?? {};

      for (const [省厅名, 省厅] of Object.entries(各省厅)) {
        const 事务次官 = 省厅.事务次官;
        if (!事务次官?.姓名) continue;

        // 事务次官所在省厅/会议地位 → 对应事务次官会议派系的核心人物同步
        const 匹配派系 = Object.entries(次官会议派系).find(
          ([, 派系]) => 派系.代表省厅 === 省厅名 || 派系.核心人物 === 事务次官.姓名,
        );
        if (匹配派系) {
          const [派系名, 派系] = 匹配派系;
          // 派系核心人物与事务次官不一致 → 以事务次官为准
          if (派系.核心人物 !== 事务次官.姓名) {
            _.set(data, ['日本执政', '事务次官会议', '内部派系', 派系名, '核心人物'], 事务次官.姓名);
          }
          // 派系代表省厅与事务次官所在省厅不一致 → 同步
          if (派系.代表省厅 !== 省厅名) {
            _.set(data, ['日本执政', '事务次官会议', '内部派系', 派系名, '代表省厅'], 省厅名);
          }
        }

        // 事务次官对阁僚态度 → 省厅内阁掌控度联动
        if (事务次官.对阁僚态度 === '架空阁僚' || 事务次官.对阁僚态度 === '暗中操控') {
          const 当前掌控度 = 省厅.内阁掌控度 ?? 50;
          if (当前掌控度 > 30) {
            _.set(data, ['日本执政', '各省厅', 省厅名, '内阁掌控度'], _.clamp(当前掌控度 - 10, 0, 100));
          }
        } else if (事务次官.对阁僚态度 === '主动配合') {
          const 当前掌控度 = 省厅.内阁掌控度 ?? 50;
          if (当前掌控度 < 70) {
            _.set(data, ['日本执政', '各省厅', 省厅名, '内阁掌控度'], _.clamp(当前掌控度 + 5, 0, 100));
          }
        }
      }

      // ═══════════════════════════════════════════════════
      // 兜底4：事件 → 当前大事件一致性校验
      // 规则详见：变量更新规则.yaml → 世界.当前大事件
      // ═══════════════════════════════════════════════════
      if (!_.isEqual(data.事件队列?.近期已发生事件, before.事件队列?.近期已发生事件)) {
        const 最新事件 = data.事件队列?.近期已发生事件?.at(-1);
        if (最新事件 && data.世界?.当前大事件 !== 最新事件.事件) {
          _.set(data, '世界.当前大事件', 最新事件.事件);
        }
      }

      // ═══════════════════════════════════════════════════
      // 兜底5：国内情势极端等级 → 潜在危机校验
      // 规则详见：变量更新规则.yaml → 日本国内情势 / 事件队列.潜在危机
      // ═══════════════════════════════════════════════════
      const 现有危机 = data.事件队列?.潜在危机 ?? [];

      // 粮食危机
      if (data.日本国内情势?.粮食供应等级 !== before.日本国内情势?.粮食供应等级) {
        const 粮食 = data.日本国内情势?.粮食供应等级;
        if ((粮食 === '饥荒边缘' || 粮食 === '黑市依赖') && !现有危机.some(c => c.危机.includes('粮食'))) {
          appendCrisis(data, 现有危机, {
            危机: '粮食危机引发大规模社会动荡',
            触发条件: '米价继续上涨或走私粮渠道中断',
            严重程度: '高',
          });
        }
      }

      // 治安危机
      if (data.日本国内情势?.治安等级 !== before.日本国内情势?.治安等级) {
        const 治安 = data.日本国内情势?.治安等级;
        if ((治安 === '无政府状态' || 治安 === '失控') && !现有危机.some(c => c.危机.includes('治安'))) {
          appendCrisis(data, 现有危机, {
            危机: '社会治安崩溃引发全面恐慌',
            触发条件: '警察体系瓦解或自警团失控',
            严重程度: '极高',
          });
        }
      }

      // 通胀危机
      if (data.日本国内情势?.通胀等级 !== before.日本国内情势?.通胀等级) {
        const 通胀 = data.日本国内情势?.通胀等级;
        if (
          (通胀 === '恶性通胀' || 通胀 === '货币崩溃') &&
          !现有危机.some(c => c.危机.includes('经济') || c.危机.includes('货币'))
        ) {
          appendCrisis(data, 现有危机, {
            危机: '恶性通胀导致经济体系崩溃',
            触发条件: 'CSDC替代日元成为事实货币',
            严重程度: '高',
          });
        }
      }

      // 互联网+选举危机
      if (data.日本国内情势?.互联网状况 !== before.日本国内情势?.互联网状况) {
        const 网络 = data.日本国内情势?.互联网状况;
        const 选举类型 = data.日本执政?.选举进程?.类型;
        if ((网络 === '大面积瘫痪' || 网络 === '严重不稳定') && 选举类型 === '2039特别大选') {
          if (!现有危机.some(c => c.危机.includes('选举') && c.危机.includes('互联网'))) {
            appendCrisis(data, 现有危机, {
              危机: '互联网断网导致特别大选无法进行',
              触发条件: '全国大面积断网持续超过48小时',
              严重程度: '极高',
            });
          }
        }
      }

      // ═══════════════════════════════════════════════════
      // 兜底6：能源供应 → 内阁稳定度校验
      // 规则详见：变量更新规则.yaml → 日本国内情势.粮食与能源
      // ═══════════════════════════════════════════════════
      if (data.日本国内情势?.能源供应等级 !== before.日本国内情势?.能源供应等级) {
        const 能源 = data.日本国内情势?.能源供应等级;
        const 当前稳定度 = data.日本执政?.本届内阁?.内阁稳定度 ?? 50;
        if (能源 === '崩溃') {
          _.set(data, '日本执政.本届内阁.内阁稳定度', _.clamp(当前稳定度 - 20, 0, 100));
        } else if (能源 === '严重短缺') {
          _.set(data, '日本执政.本届内阁.内阁稳定度', _.clamp(当前稳定度 - 10, 0, 100));
        }
      }

      // ═══════════════════════════════════════════════════
      // 兜底7：政策效果 → 内阁稳定度/事件/热点校验
      // 规则详见：变量更新规则.yaml → 日本执政.当前政策
      // ═══════════════════════════════════════════════════
      if (!_.isEqual(data.日本执政?.当前政策, before.日本执政?.当前政策)) {
        const 新政策 = data.日本执政?.当前政策 ?? {};
        const 旧政策 = before.日本执政?.当前政策 ?? {};

        for (const [政策名, 政策] of Object.entries(新政策)) {
          const 旧 = 旧政策[政策名];
          if (!_.isEqual(政策, 旧)) {
            // 政策对内阁稳定度影响
            if (政策?.对内阁稳定度影响 !== undefined && 政策?.对内阁稳定度影响 !== 旧?.对内阁稳定度影响) {
              const 当前稳定度 = data.日本执政?.本届内阁?.内阁稳定度 ?? 50;
              _.set(data, '日本执政.本届内阁.内阁稳定度', _.clamp(当前稳定度 + (政策.对内阁稳定度影响 ?? 0), 0, 100));
            }

            // 政策受阻 → 追加事件
            if (政策?.推行状态 === '受阻' && 政策?.推行状态 !== 旧?.推行状态) {
              const 现有事件 = data.事件队列?.近期已发生事件 ?? [];
              if (!现有事件.some(e => e.事件.includes(政策名) && e.事件.includes('受阻'))) {
                appendEvent(data, 现有事件, {
                  事件: `政策「${政策名}」推行受阻`,
                  时间: data.世界?.当前日期 ?? '',
                  影响: 政策?.副作用 ?? '政策推行遭遇阻力',
                });
              }
            }

            // 政策适得其反 → 追加热点
            if (政策?.效果评估 === '适得其反' && 政策?.效果评估 !== 旧?.效果评估) {
              const 现有热点 = data.舆论场?.社交媒体热点 ?? [];
              const 新热点 = `#${政策名}失败`;
              if (!现有热点.includes(新热点)) {
                _.set(data, '舆论场.社交媒体热点', [...现有热点, 新热点]);
              }
            }
          }
        }
      }

      // ═══════════════════════════════════════════════════
      // 兜底8：国际势力策略 → 国内政治势力支持度校验
      // 规则详见：变量更新规则.yaml → 国际势力动态.对日策略倾向
      // ═══════════════════════════════════════════════════
      if (!_.isEqual(data.国际势力动态, before.国际势力动态)) {
        const 势力们 = data.国际势力动态 ?? {};
        for (const [势力名, 势力] of Object.entries(势力们)) {
          const 旧势力 = (before.国际势力动态 ?? {})[势力名];
          if (!旧势力) continue;

          if (势力.对日策略倾向 === '积极扩张' && 势力.对日策略倾向 !== 旧势力.对日策略倾向) {
            const 派系关键词 = 势力名.includes('CSAT') ? '亲CSAT' : 势力名.includes('美国') ? '亲美' : null;
            if (派系关键词) {
              const 政治势力们 = data.日本执政?.主要政治势力 ?? {};
              for (const [派系名, 派系] of Object.entries(政治势力们)) {
                const shouldBoost =
                  (派系关键词 === '亲CSAT' && 派系.对CSAT态度 !== undefined && 派系.对CSAT态度 > 0) ||
                  (派系关键词 === '亲美' && 派系.对美态度 !== undefined && 派系.对美态度 > 0);
                if (shouldBoost) {
                  _.set(data, ['日本执政', '主要政治势力', 派系名, '支持度'], _.clamp((派系.支持度 ?? 0) + 5, 0, 100));
                }
              }
            }
          }
        }
      }

      // ═══════════════════════════════════════════════════
      // 兜底9：角色公职变化 → 政治卷入度校验
      // 规则详见：变量更新规则.yaml → 各角色情况
      // ═══════════════════════════════════════════════════
      for (const [角色名, 角色] of Object.entries(data.各角色情况 ?? {})) {
        const 旧角色 = (before.各角色情况 ?? {})[角色名];
        if (!旧角色) continue;

        // 担任公职变化 → 同步政治卷入度
        if (角色.担任公职 !== 旧角色.担任公职) {
          if (角色.担任公职 && 角色.担任公职 !== '无' && 角色.政治卷入度 !== '深度卷入') {
            _.set(data, ['各角色情况', 角色名, '政治卷入度'], '深度卷入');
          } else if ((!角色.担任公职 || 角色.担任公职 === '无') && 角色.政治卷入度 === '深度卷入') {
            _.set(data, ['各角色情况', 角色名, '政治卷入度'], '主动参与');
          }
        }
      }
    });
  })();
});

