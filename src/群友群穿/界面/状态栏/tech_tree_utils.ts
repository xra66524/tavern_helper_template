/** 获取研究槽中所有节点名（去重） */
export function 获取研究槽节点名(主要研究槽: 研究槽[], 次要研究槽: 研究槽[]): Set<string> {
  const names = new Set<string>();
  for (const slot of 主要研究槽) {
    if (slot.节点名) names.add(slot.节点名);
  }
  for (const slot of 次要研究槽) {
    if (slot.节点名) names.add(slot.节点名);
  }
  return names;
}

/**
 * 计算指定应用分支的完成度百分比。
 * 完成度 = 已完成节点数 / 分支总节点数 × 100
 */
export function 获取分支完成率(分支名: string, 已完成节点: Set<string>): number {
  const 分支节点 = Object.entries(科技树定义).filter(([, 定义]) => 定义.所属分支 === 分支名);
  if (分支节点.length === 0) return 0;
  const 已完成数 = 分支节点.filter(([名]) => 已完成节点.has(名)).length;
  return Math.round((已完成数 / 分支节点.length) * 100);
}

/**
 * 检查系统性科研节点的应用分支解锁条件是否满足。
 * S1-S18 节点需要对应应用分支完成度 ≥50%。
 */
export function S节点分支条件是否满足(节点名: string, 已完成节点: Set<string>): boolean {
  const 所需分支 = S节点分支条件[节点名];
  if (!所需分支) return true; // 非 S 节点或未配置条件，视为满足
  return 所需分支.every(分支名 => 获取分支完成率(分支名, 已完成节点) >= 50);
}

/**
 * 判断某节点是否可被添加到研究槽。
 * 限制条件（全部前台实现）：
 *   1. 前置科技必须全部已完成
 *   2. 节点尚未完成（不在已解锁能力清单中）
 *   3. 节点尚未在任何研究槽中
 *   4. 节点必须是 tech_tree.ts 中定义的有效节点
 *   5. 系统性科研节点还需对应应用分支完成度 ≥50%
 */
export function 节点是否可添加(节点名: string, 已完成节点: Set<string>, 已占用槽位节点: Set<string>): boolean {
  const 定义 = 科技树定义[节点名];
  if (!定义) return false; // 未定义节点
  if (已完成节点.has(节点名)) return false; // 已完成，不可再添加
  if (已占用槽位节点.has(节点名)) return false; // 已在槽中，不可重复添加

  // 前置依赖检查
  if (!定义.前置科技.every((前置名: string) => 已完成节点.has(前置名))) return false;

  // 系统性科研节点还需检查应用分支完成度
  if (定义.所属分支 === '系统性科研') {
    return S节点分支条件是否满足(节点名, 已完成节点);
  }

  return true;
}

/**
 * 获取可添加到主要研究槽的候选节点列表。
 * 过滤条件同 节点是否可添加，但额外排除已在槽中的节点。
 */
export function 获取可添加候选节点(已完成节点: Set<string>, 主要研究槽: 研究槽[], 次要研究槽: 研究槽[]): 候选节点[] {
  const 已占用 = 获取研究槽节点名(主要研究槽, 次要研究槽);
  return Object.entries(科技树定义)
    .filter(([名]) => 节点是否可添加(名, 已完成节点, 已占用))
    .map(([名, 定义]: [string, 科技节点定义]) => ({
      节点名: 名,
      描述: 定义.描述,
      所属分支: 定义.所属分支,
      研发总耗时_天: 定义.研发总耗时_天,
      前置科技: 定义.前置科技,
      解锁能力: 定义.解锁能力,
    }))
    .sort((a, b) => a.所属分支.localeCompare(b.所属分支) || a.研发总耗时_天 - b.研发总耗时_天);
}

/**
 * 获取节点的运行时状态。
 * 依次检查：已完成 → 主要研究槽 → 次要研究槽 → 前置可满足 → 锁定
 * 系统性科研节点额外检查应用分支完成度 ≥50%。
 */
export function 获取节点状态(
  节点名: string,
  已完成节点: Set<string>,
  主要研究槽: 研究槽[],
  次要研究槽: 研究槽[],
): 节点状态 {
  if (已完成节点.has(节点名)) return '已完成';

  const 在主要 = 主要研究槽.find(s => s.节点名 === 节点名);
  if (在主要) return '主要';

  const 在次要 = 次要研究槽.find(s => s.节点名 === 节点名);
  if (在次要) return '次要';

  // 检查前置是否全部完成
  const 定义 = 科技树定义[节点名];
  if (!定义) return '锁定';
  if (!定义.前置科技.every((前: string) => 已完成节点.has(前))) return '锁定';

  // 系统性科研节点额外检查应用分支完成度
  if (定义.所属分支 === '系统性科研') {
    if (!S节点分支条件是否满足(节点名, 已完成节点)) return '锁定';
  }

  return '可启动';
}

/**
 * 合并静态定义 + 运行时状态，生成所有节点的全貌列表。
 * 这是状态栏渲染 DAG 与列表的单一数据源。
 */
export function 获取所有节点全貌(已完成节点: Set<string>, 主要研究槽: 研究槽[], 次要研究槽: 研究槽[]): 节点全貌[] {
  const 层级 = 获取拓扑层级();
  return (Object.entries(科技树定义) as [string, 科技节点定义][]).map(([节点名, 定义]) => {
    const 状态 = 获取节点状态(节点名, 已完成节点, 主要研究槽, 次要研究槽);
    const 主要槽 = 主要研究槽.find(s => s.节点名 === 节点名);
    const 次要槽 = 次要研究槽.find(s => s.节点名 === 节点名);
    return {
      ...定义,
      节点名,
      状态,
      研发进度: 主要槽?.研发进度 ?? 次要槽?.研发进度 ?? 0,
      启动日期: 主要槽?.启动日期 ?? 次要槽?.启动日期 ?? '',
      拓扑层级: 层级[节点名] ?? 0,
    };
  });
}

/**
 * 按分支计算进度概览。
 * 14 个应用分支 + 系统性科研 = 15 个分组。
 */
export function 获取分支进度(已完成节点: Set<string>): 分支进度[] {
  const 分组: Record<string, string[]> = {};
  for (const [节点名, 定义] of Object.entries(科技树定义) as [string, 科技节点定义][]) {
    if (!分组[定义.所属分支]) 分组[定义.所属分支] = [];
    分组[定义.所属分支].push(节点名);
  }

  return Object.entries(分组)
    .map(([分支名, 节点列表]) => {
      const 已完成数 = 节点列表.filter(名 => 已完成节点.has(名)).length;
      return {
        分支名,
        总节点数: 节点列表.length,
        已完成数,
        进行中数: 0, // 调用时另行计算
        进度百分比: 节点列表.length > 0 ? Math.round((已完成数 / 节点列表.length) * 100) : 0,
      };
    })
    .sort((a, b) => {
      // 按分支类型排序：应用分支在前，系统性科研在后
      if (a.分支名 === '系统性科研') return 1;
      if (b.分支名 === '系统性科研') return -1;
      return a.分支名.localeCompare(b.分支名, 'zh-CN');
    });
}

/**
 * 预计完成日期计算。
 * 由于新 schema 使用研发进度(0-100)而非已研发天数，
 * 无法精确按天数计算。采用保守估算：
 *   剩余进度百分比 × 研发总耗时_天 / 100 = 剩余等效天数
 *   （即假设剩余进度按原速率推进的天数）
 */
export function 预计剩余天数(节点名: string, 研发进度: number): number {
  const 定义 = 科技树定义[节点名];
  if (!定义 || 研发进度 >= 100) return 0;
  const 剩余进度 = 100 - 研发进度;
  return Math.ceil((剩余进度 / 100) * 定义.研发总耗时_天);
}

/**
 * 检查某节点的系统性科研解锁条件是否满足。
 * S1-S18 节点需要对应应用分支完成度 ≥50%（即 Stage3 ≥ 3/6）。
 */
export function 系统性科研是否可解锁(节点名: string, 已完成节点: Set<string>): boolean {
  const 定义 = 科技树定义[节点名];
  if (!定义) return false;
  if (定义.所属分支 !== '系统性科研') return true;

  return S节点分支条件是否满足(节点名, 已完成节点);
}

