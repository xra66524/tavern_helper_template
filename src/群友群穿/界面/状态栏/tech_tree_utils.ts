// ════════════════════════════════════════════════════════════
// 从已解锁能力清单反解已完成节点名
// 条目格式：[分支] 能力描述（节点名，YYYY-MM-DD完成）
// ════════════════════════════════════════════════════════════

function abilitiesToCompletedNodes(已解锁能力清单: string[]): Set<string> {
  const names = new Set<string>();
  for (const entry of 已解锁能力清单) {
    const m = String(entry).match(/（([^，,]+)，\d{4}-\d{2}-\d{2}完成）$/);
    if (m) names.add(m[1]);
  }
  return names;
}

// ════════════════════════════════════════════════════════════
// Kahn 拓扑排序 —— 计算每个节点的层级（最长前置链长度）
// ════════════════════════════════════════════════════════════

function 计算拓扑层级(定义: Record<string, 科技节点定义>): Record<string, number> {
  const 入度: Record<string, number> = {};
  const 层级: Record<string, number> = {};

  // 初始化
  for (const 名 of Object.keys(定义)) {
    入度[名] = 定义[名].前置科技.length;
    层级[名] = 0;
  }

  // Kahn 算法
  const 队列 = Object.keys(定义).filter(名 => 入度[名] === 0);
  while (队列.length > 0) {
    const 当前 = 队列.shift()!;
    for (const [名, 节点] of Object.entries(定义)) {
      if (节点.前置科技.includes(当前)) {
        入度[名]--;
        if (入度[名] === 0) {
          队列.push(名);
        }
      }
    }
    // 层级 = 最长前置链长度
    for (const 前置 of 定义[当前]?.前置科技 ?? []) {
      层级[当前] = Math.max(层级[当前], 层级[前置] + 1);
    }
  }

  return 层级;
}

// 缓存拓扑层级结果（静态结构不变，只需计算一次）
let _拓扑层级缓存: Record<string, number> | null = null;
function 获取拓扑层级(): Record<string, number> {
  if (!_拓扑层级缓存) {
    _拓扑层级缓存 = 计算拓扑层级(科技树定义);
  }
  return _拓扑层级缓存;
}

