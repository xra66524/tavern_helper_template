//   1. 本文件定义所有科技节点的**静态结构**（前置依赖/耗时/解锁能力等）
//      这些是世界观设定的一部分，不应被 AI 在对话中修改。
//   2. MVU stat_data 只储存**运行时状态**（已研发天数/启动时间/完成时间）
//   3. 兜底脚本与状态栏通过 join 两者获取节点全貌：
//      全貌 = tech_tree.ts 静态定义 + MVU 运行时状态
//   4. 新增/修改节点 → 直接改本文件，
//      确认无误后同步改 mvu_schema.ts 和 initvar.yaml
//
// ============================================================

/** 单个科技节点的静态定义（不可被 AI 修改） */
export interface 科技节点定义 {
  /** 所属分支 */
  所属分支: string;
  /** 简要描述 */
  描述: string;
  /** 前置科技节点名列表；空数组 = 无前置 */
  前置科技: string[];
  /** 主要方向(100%速率)下从启动到完成所需总天数 */
  研发总耗时_天: number;
  /** 完成后解锁的能力标签，用于规则 11c 联动其他模块 */
  解锁能力: string[];
  /** 备注（含「依赖基地车」标记的用于规则 11d 计算科技自主度） */
  备注: string;
}

